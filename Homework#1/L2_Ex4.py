# создать словарь, который в качестве ключей будет принимать данные числа

from collections import Counter

def count_it(s): # Определение функции count_it с параметром s (строка цифр)
    digits = [int(char) for char in s] # Преобразование каждого символа строки в целое число и создание списка digits
    counter = Counter(digits)# Создание объекта Counter для подсчета частоты каждого числа в списке digits
    most_common = counter.most_common(3)# Получение списка из 3 наиболее часто встречающихся чисел с их количеством
    return dict(most_common)# Преобразование списка кортежей в словарь и возврат результата

sequence = "12345678890987654321234567898765431234567890987678987678"
result = count_it(sequence)
print(result)

print("Три наиболее часто встречающихся символа:")
# Исправление: используем items() для итерации по парам ключ-значение словаря
for char, count in result.items(): # Цикл для перебора пар ключ-значение словаря
    print(f"Символ '{char}': {count} раз(а)") # Форматированный вывод каждого символа и его количества встречаемости