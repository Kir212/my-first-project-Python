# Определение функции chess_check с тремя параметрами: our_piece, enemy_piece, enemy_type
def chess_check(our_piece, enemy_piece, enemy_type):
    # Создание словаря для преобразования буквенных координат в числовые
    letter_to_num = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5, 'f': 6, 'g': 7, 'h': 8}
    
    # Преобразование буквенной координаты вашей фигуры в числовую (например, 'd' → 4)
    x = letter_to_num[our_piece[0]]
    # Получение числовой координаты Y вашей фигуры (например, 4)
    y = our_piece[1]
    
    # Преобразование буквенной координаты вражеской фигуры в числовую (например, 'c' → 3)
    c = letter_to_num[enemy_piece[0]]
    # Получение числовой координаты Y вражеской фигуры (например, 2)
    d = enemy_piece[1]
    # enemy_piece[0] - это первый элемент кортежа, который представляет букву (например, 'c')
    # enemy_piece[1] - это второй элемент кортежа, который представляет цифру (например, 2)
    
    # Проверка, является ли вражеская фигура ферзем
    if enemy_type == 'queen':
        # Проверка угрозы от ферзя: 
        # 1. На одной вертикали (x == c)
        # 2. На одной горизонтали (y == d)
        # 3. На одной диагонали (разность по X равна разности по Y)
        return (x == c or y == d or abs(x - c) == abs(y - d))
    
    # Проверка, является ли вражеская фигура конем
    elif enemy_type == 'knight':
        # Вычисление абсолютной разницы по координате X
        dx = abs(x - c)
        # Вычисление абсолютной разницы по координате Y
        dy = abs(y - d)
        
        # Проверка угрозы от коня:
        # Конь атакует буквой "Г" - разность 2 и 1 в любом сочетании
        return (dx == 1 and dy == 2) or (dx == 2 and dy == 1)
    
    # Если тип фигуры не распознан
    else:
        return False

# Задание координат вашей фигуры
our_position = ('d', 4)
# Задание координат вражеской фигуры
enemy_position = ('c', 2)

# Проверка угрозы от ферзя
queen_check = chess_check(our_position, enemy_position, 'queen')
# Вывод результата проверки для ферзя
print(f"Ферзь угрожает: {queen_check}")

# Проверка угрозы от коня
knight_check = chess_check(our_position, enemy_position, 'knight')
# Вывод результата проверки для коня
print(f"Конь угрожает: {knight_check}")